package com.example.puzzelproject;

public class datastartplay {
    String level1;
    String level2;

    public datastartplay(String level1, String level2) {
        this.level1 = level1;
        this.level2 = level2;
    }

    public String getLevel1() {
        return level1;
    }

    public void setLevel1(String level1) {
        this.level1 = level1;
    }

    public String getLevel2() {
        return level2;
    }

    public void setLevel2(String level2) {
        this.level2 = level2;
    }
}